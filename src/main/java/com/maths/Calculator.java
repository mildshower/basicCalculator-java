package com.maths;

/**
 * a calculator
 */
public class Calculator {
  /**
   * @param num1 the number to add
   * @param num2 the number to add
   * @return added value
   * This is a cool method
   */
  public static double add(double num1, double num2) {
    return num1 + num2;
  }

  /**
   * @param num1 the number to add
   * @param num2 the number to add
   * @return sub value
   */
  public static double sub(double num1, double num2) {
    return num1 - num2;
  }
}
